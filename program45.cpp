void DisplayFactors(int iNo)
{
  int iCnt = 0;

// Time Complexity : o(N)
  for(iCnt = 1;iCnt<iNo; iCnt++)
  {
    if(iNo%iCnt== 0)
    {
        cout<<iCnt<<"\t";
    }
  }
   
}

