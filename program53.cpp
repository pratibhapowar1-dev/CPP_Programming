void CountFactorsNonFactors(int iNo)
{
  int iCnt = 0, iFrequency1 = 0, iFrequency2 =0;

  if(iNo<0)
  {
    iNo = -iNo;
  }
// Time Complexity : o(N)
  for(iCnt = 1;iCnt<iNo; iCnt++)
  {
    if(iNo % iCnt == 0)
    {
        iFrequency1++;
    }
    else
    {
        iFrequency2++;
    }
  }
 
  cout<<"Counter of factors : "<<iFrequency1<<"\n";
  cout<<"Counter of nonfactors : "<<iFrequency2<<"\n";
   
}

