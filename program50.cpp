void DisplayNonFactors(int iNo)
{
  int iCnt = 0;

  if(iNo<0)
  {
    iNo = -iNo;
  }
// Time Complexity : o(N/2)
  for(iCnt = 1;iCnt<(iNo/2); iCnt++)
  {
    if(iNo%iCnt != 0)
    {
        cout<<iCnt<<"\t";
    }
  }
   
}

///////////////////////////////////////////////////////////////////
//  Entry point function
//////////////////////////////////////////////////////////////////

int main()
{
   int iValue = 0;

   cout<<"Enter the number : ";
   cin>>iValue;
   
   DisplayNonFactors(iValue);

    return 0;
}

