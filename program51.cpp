int CountFactors(int iNo)
{
  int iCnt = 0, iFrequency = 0;

  if(iNo<0)
  {
    iNo = -iNo;
  }
// Time Complexity : o(N)
  for(iCnt = 1;iCnt<iNo; iCnt++)
  {
    if(iNo%iCnt == 0)
    {
        iFrequency++;
    }
  }
  return iFrequency;
   
}

